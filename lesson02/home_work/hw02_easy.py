# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()

print('Дан список фруктов. Напишите программу, \
выводящую фрукты в виде нумерованного списка, выровненного \
по правой стороне')
arr_fruit = ['яблоко', 'банан', 'киви', 'арбуз']
print('Дано: {0}'.format(arr_fruit))
print('Вывод:\n1.{0:>10}\n2.{1:>10}\n3.{2:>10}\n4.{3:>10}'.format(arr_fruit[0], arr_fruit[1], arr_fruit[2], arr_fruit[3]))
print('А теперь через цикл')
input('Нажмите Enter для продолжения')
print('Вывод:')
for i in range(len(arr_fruit)):
    print('{number}.{fruit:>10}'.format(number=(i+1), fruit=arr_fruit[i]))
input('Нажмите ввод для продолжения')


