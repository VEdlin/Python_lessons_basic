# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

print('Дан произвольный список из целых чисел. \
Получите НОВЫЙ список из элементов исходного, выполнив \
следующие условия: если элемент кратен двум, то разделить его \
на 4, если не кратен, то умножить на 2')
arr = [int(i) for i in str(input('Введите целые числа через пробел, \
чтобы заполнить список: ')).split(' ')]
# поскольку надо делить на 2, переводим int в float
arr1 = [float(i)/4 for i in arr if int(i) % 2 == 0] + [float(i)*2 for i in arr if int(i) % 2 != 0]
print('Новый список: ', arr1)
input('Нажмите ввод для продолжения')
